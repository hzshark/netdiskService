package cn.bestsign.auto.netdiskService.model;

public class SysLibraryInfo {

    private Integer libId;

    private String libCode;

    private String libName;

    private String libEngname;

    private String libUpcode;

    private Integer libIsvalid;

    private String libDesc;

    private String nextChildCode;

    public Integer getLibId() {
        return libId;
    }

    public void setLibId(Integer libId) {
        this.libId = libId;
    }

    public String getLibCode() {
        return libCode;
    }

    public void setLibCode(String libCode) {
        this.libCode = libCode;
    }

    public String getLibName() {
        return libName;
    }

    public void setLibName(String libName) {
        this.libName = libName;
    }

    public String getLibEngname() {
        return libEngname;
    }

    public void setLibEngname(String libEngname) {
        this.libEngname = libEngname;
    }

    public String getLibUpcode() {
        return libUpcode;
    }

    public void setLibUpcode(String libUpcode) {
        this.libUpcode = libUpcode;
    }

    public Integer getLibIsvalid() {
        return libIsvalid;
    }

    public void setLibIsvalid(Integer libIsvalid) {
        this.libIsvalid = libIsvalid;
    }

    public String getLibDesc() {
        return libDesc;
    }

    public void setLibDesc(String libDesc) {
        this.libDesc = libDesc;
    }

    public String getNextChildCode() {
        return nextChildCode;
    }

    public void setNextChildCode(String nextChildCode) {
        this.nextChildCode = nextChildCode;
    }
}