package cn.bestsign.auto.netdiskService.model;

public class SysMsg {
    private Integer msgId;

    private String msgTitle;

    private String msgDate;

    private String msgVsdate;

    private String msgVedate;

    private String msgPerson;

    private String msgContext;

    public Integer getMsgId() {
        return msgId;
    }

    public void setMsgId(Integer msgId) {
        this.msgId = msgId;
    }

    public String getMsgTitle() {
        return msgTitle;
    }

    public void setMsgTitle(String msgTitle) {
        this.msgTitle = msgTitle;
    }

    public String getMsgDate() {
        return msgDate;
    }

    public void setMsgDate(String msgDate) {
        this.msgDate = msgDate;
    }

    public String getMsgVsdate() {
        return msgVsdate;
    }

    public void setMsgVsdate(String msgVsdate) {
        this.msgVsdate = msgVsdate;
    }

    public String getMsgVedate() {
        return msgVedate;
    }

    public void setMsgVedate(String msgVedate) {
        this.msgVedate = msgVedate;
    }

    public String getMsgPerson() {
        return msgPerson;
    }

    public void setMsgPerson(String msgPerson) {
        this.msgPerson = msgPerson;
    }

    public String getMsgContext() {
        return msgContext;
    }

    public void setMsgContext(String msgContext) {
        this.msgContext = msgContext;
    }
}